int s(int n) {
  int r = 0;
  while (n) {
    r += n % 10;
    n /= 10;
  }
  return r;
}

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int a, b;
  cin >> a >> b;
  cout << max(s(a), s(b));

  return 0;
}

template <typename T>
std::vector<T> ReadArray(int size, std::istream& in_stream) {
  if (!size) {
    in_stream >> size;
  }
  std::vector<T> array(size);
  for (auto& element : array) {
    in_stream >> element;
  }
  return std::move(array);
}
